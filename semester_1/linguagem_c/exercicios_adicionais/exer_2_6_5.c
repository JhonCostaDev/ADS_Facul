#include <stdio.h>

/* Faça um programa que leia um valor do tipo float e depois o imprima usando o operador %d.
*/
int main(void){
	//Definir as variáveis
	float numero;
	
	printf("Digite um número inteiro.\n");
	scanf("%f", &numero);

	printf("O valor lido foi: %d\n", numero); //1086324736
	return 0;
}
